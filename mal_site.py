def _compute_per_lang(mal_dict, counts_dict, min_count):
    """For each language compute filtered MAL data, β(1→max), compliance."""
    out = {}
    for lang, mal_data in mal_dict.items():
        counts = counts_dict.get(lang, {})
        filt = {n: v for n, v in mal_data.items() if counts.get(n, 0) >= min_count}
        reg = compute_loglog_regression(filt, start_n=1) if filt else None
        beta = float(-reg['slope']) if reg else None
        compliance = compute_decrease_ratio(filt) if filt else None
        out[lang] = {
            'mal_filtered': filt,
            'beta_1max': beta,
            'compliance': compliance,
        }
    return out


