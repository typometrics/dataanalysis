def _render_compliance_ridgeline(per_mal, per_lmal, per_rmal,
                                 families_by_group, macro_group_order):
    """Ridgeline plot of MAL/LMAL/RMAL compliance, one ridge per macro-group.

    Three SVG panels (one per direction). Within each panel the macro-groups
    are stacked vertically; for each group we draw a Gaussian-KDE density
    of its languages' compliance scores, filled with the group's signature
    color. This is the *single-figure summary* of the abstract: it shows at
    a glance which macro-areas comply tightly with MAL, which deviate, and
    how the picture changes between left- and right-dependents.
    """
    import numpy as _np
    # Build family -> macro lookup.
    fam_to_macro = {}
    for macro, fams in families_by_group.items():
        for f in fams:
            fam_to_macro[f] = macro
    family_lookup = getattr(_render_pca_profile_map, '_family_lookup', {}) or {}
    # Aggregate compliance values per macro-group, per direction.
    PANELS = [('mal', 'MAL', per_mal),
              ('lmal', 'LMAL', per_lmal),
              ('rmal', 'RMAL', per_rmal)]
    macros_present = []
    seen = set()
    for macro, _shape in macro_group_order:
        if macro in seen:
            continue
        if families_by_group.get(macro):
            macros_present.append(macro)
            seen.add(macro)
    # group_color: first hue from the per-macro palette, defined inline below.
    GROUP_HUE = {
        'Indo-European':         '#1f77b4',
        'Afro-Asiatic':          '#d62728',
        'Other Eurasian':        '#ff7f0e',
        'Sub-Saharan':           '#2ca02c',
        'Pacific':               '#e377c2',
        'Northern Americas':     '#17becf',
        'South American':        '#ff9800',
        'Other / unclassified':  '#9e9e9e',
    }
    # Accumulate raw values per (panel, macro). per_* dicts map code -> {compliance, ...}
    samples = {}      # (key, macro) -> list[float]
    for key, _lab, src in PANELS:
        for code, rec in (src or {}).items():
            val = rec.get('compliance') if isinstance(rec, dict) else rec
            try:
                fval = float(val)
            except (TypeError, ValueError):
                continue
            if _np.isnan(fval):
                continue
            fam = family_lookup.get(code)
            if not fam:
                continue
            macro = fam_to_macro.get(fam, 'Other / unclassified')
            samples.setdefault((key, macro), []).append(fval)

    # Pre-compute KDE on a shared grid so the three panels share an x-axis.
    all_vals = [v for vs in samples.values() for v in vs]
    if not all_vals:
        return ''
    x_lo, x_hi = float(min(all_vals)), float(max(all_vals))
    pad = 0.05 * (x_hi - x_lo + 1e-9)
    x_lo -= pad; x_hi += pad
    grid = _np.linspace(x_lo, x_hi, 120)

    def _kde(vals):
        a = _np.asarray(vals, dtype=float)
        n = len(a)
        if n < 2:
            return None
        sigma = float(a.std(ddof=1))
        if sigma == 0:
            sigma = 1e-6
        bw = 1.06 * sigma * (n ** (-1.0 / 5.0))   # Silverman's rule
        # density at each grid point (no scipy).
        d = _np.exp(-0.5 * ((grid[:, None] - a[None, :]) / bw) ** 2)
        d = d.sum(axis=1) / (n * bw * (2 * _np.pi) ** 0.5)
        return d

    # Build per-panel ridges JSON: list of {macro, color, n, mean, density}.
    panels_data = {}
    panel_max = {}
    for key, lab, _src in PANELS:
        ridges = []
        peak = 0.0
        for macro in macros_present:
            vals = samples.get((key, macro), [])
            if len(vals) < 2:
                continue
            d = _kde(vals)
            if d is None:
                continue
            peak = max(peak, float(d.max()))
            ridges.append({
                'macro': macro,
                'color': GROUP_HUE.get(macro, '#888'),
                'n': len(vals),
                'mean': float(_np.mean(vals)),
                'density': [float(v) for v in d],
            })
        panels_data[key] = ridges
        panel_max[key] = peak

    import json
    panels_json = json.dumps(panels_data)
    grid_json = json.dumps([float(v) for v in grid])
    panel_max_json = json.dumps(panel_max)

    intro = ('<h2>Compliance ridgeline by macro-area</h2>'
             '<p class="info-box">For each direction (MAL, LMAL, RMAL) we draw '
             'a Gaussian density of per-language compliance, one ridge per '
             'macro-group. Tight ridges centered near 1 indicate areas that '
             'follow Menzerath\u2019s law strictly; broad or shifted ridges '
             'mark areas that deviate. The contrast between the LMAL and '
             'RMAL panels is the asymmetry summary chart for the paper.</p>')

    html = []
    html.append('<div class="ridge-block">')
    html.append(intro)
    html.append('<div id="malRidge"></div>')
    html.append(f'''
<style>
.ridge-block #malRidge {{ display: flex; gap: 12px; flex-wrap: wrap;
                          justify-content: center; margin: 8px 0 16px; }}
.ridge-block .ridge-panel {{ flex: 1 1 320px; max-width: 460px; min-width: 280px; }}
.ridge-block .ridge-panel h3 {{ margin: 0 0 4px 0; font-size: 14px;
                                text-align: center; color: #333; }}
.ridge-block svg.ridge-svg {{ width: 100%; height: auto; display: block; }}
.ridge-block path.ridge {{ fill-opacity: 0.55; stroke-width: 1.1; }}
.ridge-block line.ridge-mean {{ stroke: #222; stroke-width: 1; stroke-dasharray: 3 2; }}
.ridge-block text.ridge-label {{ font-size: 10px; fill: #333; }}
.ridge-block text.ridge-axis {{ font-size: 9px; fill: #777; }}
</style>
<script>
(function() {{
  const PANELS  = [['mal','MAL'],['lmal','LMAL'],['rmal','RMAL']];
  const DATA    = {panels_json};
  const GRID    = {grid_json};
  const PEAK    = {panel_max_json};
  const W = 440, H = 280, M = {{l: 38, r: 12, t: 18, b: 24}};
  const x_lo = GRID[0], x_hi = GRID[GRID.length - 1];
  const x = d3.scaleLinear().domain([x_lo, x_hi]).range([M.l, W - M.r]);
  const root = d3.select('#malRidge');

  PANELS.forEach(([key, label]) => {{
    const ridges = DATA[key] || [];
    const panel = root.append('div').attr('class', 'ridge-panel');
    panel.append('h3').text(label + ' compliance');
    const svg = panel.append('svg').attr('class', 'ridge-svg')
      .attr('viewBox', '0 0 ' + W + ' ' + H);
    if (!ridges.length) {{
      svg.append('text').attr('x', W/2).attr('y', H/2)
         .attr('text-anchor', 'middle').attr('fill', '#888')
         .text('no data');
      return;
    }}
    // Vertical layout: each ridge gets a horizontal slot.
    const slotH = (H - M.t - M.b) / ridges.length;
    // Cap each ridge so the tallest density only fills 90 % of its slot —
    // ridges with few samples don't tower over their neighbours.
    const peak = PEAK[key] || 1;
    const yScaleRidge = d => (slotH * 0.9) * (d / peak);
    ridges.forEach((r, i) => {{
      const y0 = M.t + i * slotH + slotH * 0.95;     // baseline of ridge
      // Build closed polygon: density curve + baseline.
      let pts = GRID.map((g, j) =>
        x(g) + ',' + (y0 - yScaleRidge(r.density[j])));
      pts.push(x(x_hi) + ',' + y0);
      pts.push(x(x_lo) + ',' + y0);
      svg.append('polygon').attr('class', 'ridge')
         .attr('points', pts.join(' '))
         .attr('fill', r.color).attr('stroke', r.color);
      // Mean tick.
      svg.append('line').attr('class', 'ridge-mean')
         .attr('x1', x(r.mean)).attr('x2', x(r.mean))
         .attr('y1', y0).attr('y2', y0 - slotH * 0.85);
      // Group label, left-aligned at panel left edge.
      svg.append('text').attr('class', 'ridge-label')
         .attr('x', M.l + 3).attr('y', y0 - slotH * 0.45)
         .text(r.macro + '  (n=' + r.n + ')');
    }});
    // X-axis ticks.
    const ticks = x.ticks(5);
    ticks.forEach(t => {{
      svg.append('line').attr('x1', x(t)).attr('x2', x(t))
         .attr('y1', H - M.b).attr('y2', H - M.b + 3)
         .attr('stroke', '#aaa');
      svg.append('text').attr('class', 'ridge-axis')
         .attr('x', x(t)).attr('y', H - M.b + 12)
         .attr('text-anchor', 'middle')
         .text(d3.format('.2f')(t));
    }});
    svg.append('text').attr('class', 'ridge-axis')
       .attr('x', (M.l + W - M.r) / 2).attr('y', H - 4)
       .attr('text-anchor', 'middle')
       .text('compliance score');
  }});
}})();
</script>
''')
    html.append('</div>')
    return ''.join(html)


