def _pca_2d(feature_dict):
    """Standardize and PCA-project a {code: [features]} dict.

    Returns ``(codes, coords[N,2], explained[2], loadings[F,2], keep_mask)``
    or ``None`` when not enough languages or features. Standardization
    (z-score per feature) is applied so heterogeneous units contribute
    comparably. ``loadings[j,k] = S[k] * V[j,k] / sqrt(N)`` is the
    correlation-style loading of feature j on PC k (the standard biplot
    "variable arrow" convention). ``keep_mask`` flags which input feature
    columns survived the all-NaN filter.
    """
    import numpy as _np
    codes = sorted(feature_dict.keys())
    if len(codes) < 4:
        return None
    X = _np.array([feature_dict[c] for c in codes], dtype=float)
    keep = ~_np.all(_np.isnan(X), axis=0)
    X = X[:, keep]
    if X.shape[1] < 2:
        return None
    col_means = _np.nanmean(X, axis=0)
    nan_idx = _np.where(_np.isnan(X))
    X[nan_idx] = _np.take(col_means, nan_idx[1])
    X = X - X.mean(axis=0, keepdims=True)
    stds = X.std(axis=0, keepdims=True)
    stds[stds == 0] = 1.0
    X = X / stds
    try:
        U, S, Vt = _np.linalg.svd(X, full_matrices=False)
    except _np.linalg.LinAlgError:
        return None
    coords = U[:, :2] * S[:2]
    total = (S ** 2).sum() or 1.0
    explained = (S[:2] ** 2) / total
    # Correlation-style loadings (each column = unit-variance principal
    # direction scaled by its singular value / sqrt(N)).
    loadings = (Vt[:2, :].T) * (S[:2] / _np.sqrt(max(X.shape[0], 1)))
    return codes, coords, explained, loadings, keep


