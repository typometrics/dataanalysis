def _r2_distribution_html(r2_distribution, dir_label):
    if not r2_distribution:
        return '<p><em>Not enough data for R² distribution.</em></p>'
    values = [d['r_squared'] for d in r2_distribution]
    bin_w = 0.05
    bins = [0] * 20
    for v in values:
        idx = min(int(v / bin_w), 19)
        bins[idx] += 1
    labels = [f'{i*bin_w:.2f}–{(i+1)*bin_w:.2f}' for i in range(20)]
    data_json = json.dumps({'labels': labels, 'bins': bins, 'count': len(values),
                            'mean': float(np.mean(values)),
                            'median': float(np.median(values))})
    return f'''
<div class="chart-container">
<h3>R² Goodness-of-Fit Distribution for β(1→max) regressions ({dir_label})</h3>
<canvas id="r2dist_{dir_label.lower()}" height="100"></canvas>
</div>

<script>
(function() {{
    const d = {data_json};
    const colors = d.bins.map((_, i) => i >= 18 ? 'rgba(13,71,161,0.85)'
                                : i >= 14 ? 'rgba(30,136,229,0.8)'
                                : i >= 10 ? 'rgba(100,181,246,0.75)'
                                : 'rgba(187,222,251,0.7)');
    new Chart(document.getElementById('r2dist_{dir_label.lower()}'), {{
        type: 'bar', data: {{labels: d.labels, datasets: [{{
            label: 'Number of languages', data: d.bins,
            backgroundColor: colors, borderWidth: 1
        }}]}},
        options: {{ responsive: true, plugins: {{legend: {{display: false}}}},
            scales: {{
                x: {{title: {{display: true, text: 'R² value'}}}},
                y: {{title: {{display: true, text: 'Number of languages'}}, beginAtZero: true}}
            }}
        }}
    }});
}})();
</script>
<div class="explanation"><p>Mean R² = {{mean}}, median R² = {{median}}, n = {{count}}.</p></div>
'''.replace('{mean}', f'{np.mean(values):.3f}'
            ).replace('{median}', f'{np.median(values):.3f}'
            ).replace('{count}', str(len(values)))


