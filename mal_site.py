def generate_site(output_dir,
                  lang2MAL_total, lang2MAL_left, lang2MAL_right,
                  lang2counts_total, lang2counts_left, lang2counts_right,
                  langNames, langnameGroup, lang_to_vo=None,
                  wals_languages_path=None, min_count=None,
                  n_axis_max=DEFAULT_N_AXIS_MAX,
                  notebook_path='08_menzerath_altmann_analysis.ipynb',
                  plots_dir='plots',
                  paper_pdf_path='latex/main.pdf',
                  examples_mode='reuse',
                  examples_languages=None,
                  examples_max_per_bucket=20,
                  examples_max_sentences=None,
                  examples_workers=None):
    """Build the complete UDW26 companion site under ``output_dir``.

    ``examples_mode`` controls how per-language sample-sentence pages are
    handled:

    - ``'reuse'``  : look for ``<output_dir>/examples/<lang_code>/index.html``
                     (do not regenerate).
    - ``'regen'``  : (re)generate them in-place via ``generate_mal_examples``.
    - ``'none'``   : skip the examples integration entirely.
    """
    if min_count is None:
        min_count = DEFAULT_MIN_COUNT
    os.makedirs(output_dir, exist_ok=True)

    # Sample-sentence examples (per-language MAL/LMAL/RMAL pages).
    global _EXAMPLES_INDEX
    _EXAMPLES_INDEX = {}
    if examples_mode == 'regen':
        try:
            from generate_mal_examples import generate_examples
            generate_examples(
                data_dir='data',
                output_dir=os.path.join(output_dir, 'examples'),
                languages=examples_languages,
                max_per_bucket=examples_max_per_bucket,
                max_sentences=examples_max_sentences,
                n_workers=examples_workers,
            )
        except Exception as exc:
            print(f"⚠ Could not regenerate MAL examples: {exc}")
    if examples_mode in ('reuse', 'regen'):
        _EXAMPLES_INDEX = _build_examples_index(output_dir, langNames)
        n_with = len(_EXAMPLES_INDEX)
        if n_with:
            print(f"✓ Sample-sentence examples linked for {n_with} language(s) "
                  f"(under {os.path.join(output_dir, 'examples')})")
            try:
                from generate_mal_examples import write_examples_homepage
                write_examples_homepage(
                    os.path.join(output_dir, 'examples'), langNames)
            except Exception as exc:
                print(f"⚠ Could not write examples homepage: {exc}")
        else:
            print(f"  (no per-language examples found under "
                  f"{os.path.join(output_dir, 'examples')}; "
                  f"run with --examples-mode regen to create them)")

    # Filter MAL dicts by min_count for everything downstream
    mal_total_f = _filter_mal_by_count(lang2MAL_total, lang2counts_total, min_count)
    mal_left_f = _filter_mal_by_count(lang2MAL_left, lang2counts_left, min_count)
    mal_right_f = _filter_mal_by_count(lang2MAL_right, lang2counts_right, min_count)

    # Per-language β(1→max) and compliance
    per_mal = _compute_per_lang(lang2MAL_total, lang2counts_total, min_count)
    per_lmal = _compute_per_lang(lang2MAL_left, lang2counts_left, min_count)
    per_rmal = _compute_per_lang(lang2MAL_right, lang2counts_right, min_count)

    # Annotate per_lang dicts with raw mal_full + counts so detail builders can render every cell
    for lang in per_mal:
        per_mal[lang]['mal_full'] = lang2MAL_total.get(lang, {})
        per_mal[lang]['counts'] = lang2counts_total.get(lang, {})
    for lang in per_lmal:
        per_lmal[lang]['mal_full'] = lang2MAL_left.get(lang, {})
        per_lmal[lang]['counts'] = lang2counts_left.get(lang, {})
    for lang in per_rmal:
        per_rmal[lang]['mal_full'] = lang2MAL_right.get(lang, {})
        per_rmal[lang]['counts'] = lang2counts_right.get(lang, {})

    # Global axis bounds shared by ALL mini regression plots site-wide.
    global_bounds = compute_global_scale(
        mal_total_f, mal_left_f, mal_right_f,
        force_min_n=1, padding=0.08)

    # max_n for detail tables = max over the data
    def _max_n(d):
        return max((max(v.keys(), default=0) for v in d.values()), default=0)
    max_n_total = _max_n(lang2MAL_total)
    max_n_left = _max_n(lang2MAL_left)
    max_n_right = _max_n(lang2MAL_right)

    written = []
    # Copy paper PDF next to the site so the index can link to it.
    paper_pdf_available = False
    if paper_pdf_path and os.path.exists(paper_pdf_path):
        dest = os.path.join(output_dir, PAPER_PDF_FILENAME)
        try:
            shutil.copy2(paper_pdf_path, dest)
            paper_pdf_available = True
        except OSError:
            paper_pdf_available = False
    written.append(generate_index_html(
        output_dir, per_mal, per_lmal, per_rmal,
        langNames, global_bounds, n_axis_max,
        paper_pdf_available=paper_pdf_available))
    written.append(generate_mal_effect_html(
        output_dir, per_mal, per_lmal, per_rmal,
        langNames, langnameGroup, lang_to_vo,
        global_bounds, n_axis_max, min_count))
    for direction, per_lang in (('mal', per_mal), ('lmal', per_lmal), ('rmal', per_rmal)):
        written.append(generate_more_effect_html(
            output_dir, direction, per_lang, langNames, langnameGroup,
            lang_to_vo, wals_languages_path, min_count))
    written.append(generate_mal_compliance_index_html(output_dir))
    written.append(generate_mal_compliance_summary_html(
        output_dir, per_mal, per_lmal, per_rmal,
        langNames, langnameGroup, lang_to_vo, min_count))
    for direction, per_lang, mxn in (('mal', per_mal, max_n_total),
                                      ('lmal', per_lmal, max_n_left),
                                      ('rmal', per_rmal, max_n_right)):
        written.append(generate_mal_compliance_detail_html(
            output_dir, direction, per_lang, langNames, langnameGroup,
            lang_to_vo, global_bounds, n_axis_max, mxn, min_count))

    commentary = generate_commentary_html(output_dir, notebook_path=notebook_path,
                                          plots_dir=plots_dir)
    if commentary:
        written.append(commentary)

    # ----- New pages: paper-aligned reproductions and interactive explorer -----
    written.append(generate_paper_figures_html(
        output_dir, per_mal, per_lmal, per_rmal,
        langNames, global_bounds, n_axis_max))
    written.append(generate_statistical_tests_html(
        output_dir, per_mal, per_lmal, per_rmal,
        langNames, langnameGroup, lang_to_vo))
    written.append(generate_typological_maps_html(
        output_dir, lang2MAL_total, langNames, langnameGroup,
        lang_to_vo, wals_languages_path,
        lang2MAL_left=lang2MAL_left, lang2MAL_right=lang2MAL_right,
        per_mal=per_mal, per_lmal=per_lmal, per_rmal=per_rmal))
    written.append(generate_explorer_html(
        output_dir, lang2MAL_total, lang2MAL_left, lang2MAL_right,
        lang2counts_total, lang2counts_left, lang2counts_right,
        langNames, langnameGroup, lang_to_vo, min_count))
    written.append(generate_directional_histograms_html(
        output_dir, per_mal, per_lmal, per_rmal, langNames, lang_to_vo))
    written.append(generate_data_coverage_html(
        output_dir, lang2counts_total, lang2counts_left, lang2counts_right,
        min_count, langNames))

    return {
        'output_dir': output_dir,
        'pages': written,
        'global_bounds': global_bounds,
        'n_languages': len(per_mal),
        'min_count': min_count,
    }


