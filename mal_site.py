def _load_inputs_from_disk(data_dir='data'):
    """Load all inputs needed by ``generate_site`` from cached files.

    Returns a dict suitable for splatting into ``generate_site`` (plus
    ``min_count`` left to the caller).
    """
    import pickle
    import pandas as pd

    # Metadata: langNames, langnameGroup
    with open(os.path.join(data_dir, 'metadata.pkl'), 'rb') as f:
        metadata = pickle.load(f)
    langNames = metadata['langNames']
    langnameGroup = metadata['langnameGroup']

    # MAL scores: {lang: {'total': {n: mal}, 'left': {...}, 'right': {...}}}
    with open(os.path.join(data_dir, 'lang2MAL_full.pkl'), 'rb') as f:
        lang2MAL = pickle.load(f)

    # Sample counts per n (full + directional) — derived from position2num
    with open(os.path.join(data_dir, 'all_langs_position2num.pkl'), 'rb') as f:
        all_langs_position2num = pickle.load(f)
    lang2counts_total = _mhr.get_sample_counts_per_n(all_langs_position2num)
    lang2counts_left, lang2counts_right = _mhr.get_directional_counts(all_langs_position2num)

    lang2MAL_total = {l: d['total'] for l, d in lang2MAL.items() if d.get('total')}
    lang2MAL_left = {l: d['left'] for l, d in lang2MAL.items() if d.get('left')}
    lang2MAL_right = {l: d['right'] for l, d in lang2MAL.items() if d.get('right')}

    # VO/HI scores (optional)
    lang_to_vo = None
    vo_path = os.path.join(data_dir, 'vo_vs_hi_scores.csv')
    if os.path.exists(vo_path):
        vo_df = pd.read_csv(vo_path)
        if not vo_df.empty and 'vo_score' in vo_df.columns:
            join_col = 'language_code' if 'language_code' in vo_df.columns else 'language'
            lang_to_vo = {row[join_col]: row['vo_score']
                          for _, row in vo_df.iterrows()
                          if pd.notna(row['vo_score'])}

    wals_path = os.path.join(data_dir, 'wals', 'languages.csv')
    if not os.path.exists(wals_path):
        wals_path = None

    return dict(
        lang2MAL_total=lang2MAL_total,
        lang2MAL_left=lang2MAL_left,
        lang2MAL_right=lang2MAL_right,
        lang2counts_total=lang2counts_total,
        lang2counts_left=lang2counts_left,
        lang2counts_right=lang2counts_right,
        langNames=langNames,
        langnameGroup=langnameGroup,
        lang_to_vo=lang_to_vo,
        wals_languages_path=wals_path,
    )


def main(argv=None):
    import argparse
    p = argparse.ArgumentParser(
        description='Generate the UDW26/LREC 2026 MAL companion site from cached data.')
    p.add_argument('--data-dir', default='data',
                   help='Directory containing metadata.pkl, lang2MAL_full.pkl, '
                        'all_langs_position2num.pkl, vo_vs_hi_scores.csv (default: data)')
    p.add_argument('--output-dir', default='html_analyses',
                   help='Output directory for the HTML site (default: html_analyses)')
    p.add_argument('--min-count', type=int, default=DEFAULT_MIN_COUNT,
                   help=f'Minimum sample count per (lang, n) cell (default: {DEFAULT_MIN_COUNT})')
    p.add_argument('--notebook', default='08_menzerath_altmann_analysis.ipynb',
                   help='Path to the analysis notebook used to build the commentary page')
    p.add_argument('--plots-dir', default='plots',
                   help='Directory containing PNG plots referenced by the notebook (default: plots)')
    p.add_argument('--paper-pdf', default='latex/main.pdf',
                   help='Path to the compiled paper PDF; copied next to the site if it exists '
                        '(default: latex/main.pdf)')
    p.add_argument('--examples-mode', choices=('reuse', 'regen', 'none'),
                   default='reuse',
                   help="Per-language sample-sentence pages: 'reuse' (default) "
                        "links existing pages under <output-dir>/examples/, "
                        "'regen' (re)builds them via generate_mal_examples.py, "
                        "'none' skips the integration entirely.")
    p.add_argument('--examples-languages', default=None,
                   help='Comma-separated language codes to (re)generate '
                        '(default: all). Only used with --examples-mode regen.')
    p.add_argument('--examples-max-per-bucket', type=int, default=20,
                   help='Reservoir size per (direction, n) sample page '
                        '(default: 8). Only used with --examples-mode regen.')
    p.add_argument('--examples-max-sentences', type=int, default=None,
                   help='Cap on parsed sentences per language for example '
                        'extraction (default: no cap). Only used with '
                        '--examples-mode regen.')
    p.add_argument('--examples-workers', type=int, default=None,
                   help='Worker processes for example generation '
                        '(default: half the CPUs). Only used with '
                        '--examples-mode regen.')
    args = p.parse_args(argv)

    inputs = _load_inputs_from_disk(args.data_dir)
    examples_languages = None
    if args.examples_languages:
        examples_languages = [l.strip() for l in args.examples_languages.split(',')
                              if l.strip()]
    stats = generate_site(output_dir=args.output_dir, min_count=args.min_count,
                          notebook_path=args.notebook, plots_dir=args.plots_dir,
                          paper_pdf_path=args.paper_pdf,
                          examples_mode=args.examples_mode,
                          examples_languages=examples_languages,
                          examples_max_per_bucket=args.examples_max_per_bucket,
                          examples_max_sentences=args.examples_max_sentences,
                          examples_workers=args.examples_workers,
                          **inputs)

    print(f"\u2713 Site generated under {stats['output_dir']}/")
    print(f"  - {stats['n_languages']} languages")
    print(f"  - min_count = {stats['min_count']}")
    print(f"  - global log-space mini-plot bounds: {stats['global_bounds']}")
    for path in stats['pages']:
        print(f"    \u00b7 {path}")
    return 0


