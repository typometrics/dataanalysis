def _beta_category(beta):
    """Return ('mal'|'anti'|'gray', label) using paper thresholds ±0.1.

    Mirrors :func:`_beta_effect_label` — in this codebase ``beta`` is already
    the *negated* log-log slope (positive = MAL compliance).
    """
    if beta is None:
        return ('na', '—')
    if beta > 0.1:
        return ('mal', 'MAL')
    if beta < -0.1:
        return ('anti', 'Anti-MAL')
    return ('gray', 'gray zone')


def _vo_category(vo_score):
    """Paper §1 footnote: VO if score > 2/3 (i.e. VO order at least twice more
    widespread than OV), OV if score < 1/3, NDO (no dominant order) otherwise.
    The 2/3 cutoff (≈0.667) is the rule actually applied to the data — it
    reproduces the paper's Table 1b totals (NDO=20, OV=38, VO=73)."""
    if vo_score is None:
        return None
    if vo_score > 2/3:
        return 'VO'
    if vo_score < 1/3:
        return 'OV'
    return 'NDO'


def _categorize_languages(per_lang, langNames, langnameGroup, lang_to_vo):
    """Return list of dicts: {code, name, ie, vo, cat} for languages with beta."""
    out = []
    for lang, entry in per_lang.items():
        beta = entry.get('beta_1max')
        if beta is None:
            continue
        name = langNames.get(lang, lang)
        group = langnameGroup.get(name, 'Other') if langnameGroup else 'Other'
        ie = 'IE' if group == 'Indo-European' else 'Non-IE'
        vo = _vo_category(lang_to_vo.get(lang) if lang_to_vo else None)
        cat, _ = _beta_category(beta)
        out.append({'code': lang, 'name': name, 'ie': ie, 'vo': vo, 'cat': cat})
    return out


def _crosstab(items, axis_key, axis_values, cat_key='cat',
              cat_values=('anti', 'gray', 'mal')):
    """Return dict[cat][axis] = count, plus totals per axis and grand total."""
    table = {c: {a: 0 for a in axis_values} for c in cat_values}
    for it in items:
        a = it.get(axis_key)
        c = it.get(cat_key)
        if a in axis_values and c in cat_values:
            table[c][a] += 1
    return table


def _paper_table_html(caption, axis_label, axis_values, table, dir_label,
                      label_high='MAL', label_mid='gray zone', label_low='Anti-MAL',
                      row_classes=('row-anti', 'row-gray', 'row-mal'),
                      cat_order=('anti', 'gray', 'mal'),
                      label_for_cat=None):
    """Render a small 'paper-style' contingency table with totals."""
    if label_for_cat is None:
        label_for_cat = {'anti': f'Anti-{dir_label}',
                         'gray': 'gray zone',
                         'mal': dir_label,
                         'high': 'high',
                         'middle': 'middle',
                         'low': 'low'}
    out = ['<div class="paper-table"><table>']
    out.append(f'<caption>{caption}</caption>')
    out.append('<thead><tr>')
    out.append(f'<th style="text-align:left;">{axis_label}</th>')
    for a in axis_values:
        out.append(f'<th>{a}</th>')
    out.append('<th class="tot-col">Total</th>')
    out.append('</tr></thead><tbody>')
    col_totals = {a: 0 for a in axis_values}
    grand = 0
    for cat, css in zip(cat_order, row_classes):
        row = table.get(cat, {})
        row_total = sum(row.get(a, 0) for a in axis_values)
        out.append('<tr>')
        out.append(f'<td class="{css}" style="text-align:left;font-weight:bold;">'
                   f'{label_for_cat.get(cat, cat)}</td>')
