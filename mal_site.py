    for members in clusters.values():
        n = len(members)
        if n < 2:
            continue
        # Radius grows with cluster size but stays modest so the cloud
        # still reads as one location.
        r = min(4 + 2.5 * n, 32)
        cx = sum(pts[i]['x'] for i in members) / n
        cy = sum(pts[i]['y'] for i in members) / n
        for k, i in enumerate(members):
            ang = 2 * _math.pi * k / n
            pts[i]['x'] = cx + r * _math.cos(ang)
            pts[i]['y'] = cy + r * _math.sin(ang)
        fan_rings.append({'cx': float(cx), 'cy': float(cy),
                          'r': float(r), 'n': n})

