def _markdown_to_html(md_text):
    """Convert Markdown to HTML, including tables and fenced code.

    Uses python-markdown with the ``tables`` and ``fenced_code`` extensions.
    """
    import markdown as _md
    return _md.markdown(
        md_text,
        extensions=['tables', 'fenced_code', 'sane_lists'],
        output_format='html5',
    )


def _extract_plot_files_from_code(code_source):
    """Extract plot filenames a code cell saves with ``plt.savefig``/``fig.savefig``.

    Recognizes:
      - ``.savefig('plots/foo.png')``
      - ``.savefig(os.path.join(PLOTS_DIR, 'foo.png'))``
      - ``.savefig(os.path.join(plots_dir, 'foo.png'))``
    Returns a list of paths relative to the workspace root, deduplicated.
    """
    import re
    files = []
    files += re.findall(r"\.savefig\s*\(\s*['\"]([^'\"]+)['\"]", code_source)
    for m in re.findall(
            r"\.savefig\s*\(\s*os\.path\.join\s*\(\s*(?:PLOTS_DIR|plots_dir)\s*,\s*['\"]([^'\"]+)['\"]",
            code_source):
        files.append(os.path.join('plots', m))
    seen, out = set(), []
    for f in files:
        if f not in seen:
            seen.add(f)
            out.append(f)
    return out


