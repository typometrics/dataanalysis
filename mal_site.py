    series = {
        'MAL':  values(per_lang_mal),
        'LMAL': values(per_lang_lmal),
        'RMAL': values(per_lang_rmal),
    }
    # 0.05-wide bins
    all_vals = sum(series.values(), [])
    if not all_vals:
        return '<p><em>No β(1→max) data available for histogram.</em></p>'
    bin_w = 0.05
    lo = float(np.floor(min(all_vals) / bin_w) * bin_w)
    hi = float(np.ceil(max(all_vals) / bin_w) * bin_w) + bin_w
    edges = []
    cur = lo
    while cur < hi + 1e-9:
        edges.append(cur)
        cur += bin_w
    centers = [(edges[i] + edges[i + 1]) / 2 for i in range(len(edges) - 1)]
    labels = [f'{c:+.2f}' for c in centers]

