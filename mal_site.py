def _build_detail_table(per_lang, langNames, langnameGroup, lang_to_vo,
                       global_bounds, n_axis_max, max_n, min_count, dir_label,
                       table_id='detailTable'):
    """The full per-direction MAL_n table:
    Lang | Family | VO | β(1→max) (clickable) | MAL compliance | MAL_1 | β(1→2) | MAL_2 | β(2→3) | …
    """
    # Compute local change scores
    mal_filtered = {l: d['mal_filtered'] for l, d in per_lang.items() if d['mal_filtered']}
    local_scores = compute_local_scores_for_all_languages(mal_filtered)

    # Resolve mal_label for popup title
    mal_label = {'MAL': 'MAL', 'LMAL': 'Left MAL', 'RMAL': 'Right MAL'}.get(dir_label, dir_label)

    rows = sorted(((lang, langNames.get(lang, lang)) for lang in per_lang),
                  key=lambda x: x[1].lower())
    out = []
    out.append(f'<table id="{table_id}"><thead><tr>')
    out.append(f'<th onclick="sortTable(\'{table_id}\',0,\'string\')" style="cursor:pointer">Language ⇅</th>')
    out.append(f'<th class="group-col" onclick="sortTable(\'{table_id}\',1,\'string\')" style="cursor:pointer">Family ⇅</th>')
    out.append(f'<th class="vo-col" onclick="sortTable(\'{table_id}\',2,\'number\')" style="cursor:pointer">VO ⇅</th>')
    out.append(f'<th class="effect-col" onclick="sortTable(\'{table_id}\',3,\'number\')" style="cursor:pointer;min-width:160px">{dir_label} β(1→max) ⇅</th>')
    out.append(f'<th class="conform-col" onclick="sortTable(\'{table_id}\',4,\'number\')" style="cursor:pointer">{dir_label} compliance ⇅</th>')
    col_idx = 5
    for n in range(1, max_n + 1):
        out.append(f'<th class="mal-col" onclick="sortTable(\'{table_id}\',{col_idx},\'number\')" '
                   f'style="cursor:pointer">MAL_{n}<br>(count) ⇅</th>')
        col_idx += 1
        if n < max_n:
            out.append(f'<th class="score-col" onclick="sortTable(\'{table_id}\',{col_idx},\'number\')" '
                       f'style="cursor:pointer">{n}→{n+1} ⇅</th>')
            col_idx += 1
    out.append('</tr></thead><tbody>')

    for lang, name in rows:
        entry = per_lang[lang]
        group = langnameGroup.get(name, 'Other') if langnameGroup else 'Other'
        vo = lang_to_vo.get(lang) if lang_to_vo else None
        mal_filt = entry.get('mal_filtered', {})
        mal_data = entry.get('mal_full', mal_filt)
        counts = entry.get('counts', {})
        scores = local_scores.get(lang, {})

        out.append(f'<tr id="lang-{lang}">')
        out.append(f'<td class="lang-name">{name}{_examples_link_html(lang)}</td>')
        out.append(f'<td class="group-cell">{group}</td>')
        out.append(_vo_cell(vo))
        svg, beta = generate_loglog_svg(
            mal_filt, start_n=1, width=140, height=110,
            lang_name=name, lang_code=lang, mal_label=mal_label,
            fixed_bounds=global_bounds, n_axis_max=n_axis_max)
        out.append(_effect_cell_with_plot(svg, beta))
        out.append(_compliance_cell(entry.get('compliance')))

        for n in range(1, max_n + 1):
            if n in mal_data:
                v = mal_data[n]
                c = counts.get(n, 0)
                if c >= min_count:
                    out.append(f'<td class="mal-cell" data-value="{v:.6f}">{v:.3f}<br>({c})</td>')
                else:
                    out.append(f'<td class="mal-cell-lowcount" data-value="{v:.6f}" '
                               f'title="Low count (n={c}, threshold={min_count})">'
                               f'{v:.3f}<br>({c})</td>')
            else:
                out.append('<td class="na-cell" data-value="">—</td>')
            if n < max_n:
                key = f'{n}→{n+1}'
                if key in scores and not np.isnan(scores[key]):
                    sc = scores[key]
                    cn = counts.get(n, 0)
                    cn1 = counts.get(n + 1, 0)
                    if cn < min_count or cn1 < min_count:
                        css = 'score-cell-lowcount'
                    elif sc > 0.1:
                        css = 'score-cell score-positive'
                    elif sc < -0.1:
                        css = 'score-cell score-negative'
                    else:
                        css = 'score-cell score-neutral'
                    out.append(f'<td class="{css}" data-value="{sc:.6f}">{sc:.3f}</td>')
                else:
                    out.append('<td class="na-cell" data-value="">—</td>')
        out.append('</tr>')
    out.append('</tbody></table>')
    return ''.join(out)


def _slope_summary_table_html(per_lang, max_n=5):
    """Adapted from mal_html_report._generate_slope_summary_table but with
    β(1→max) instead of β(2→max)."""
    cols = []
    vals_1max = [d['beta_1max'] for d in per_lang.values() if d['beta_1max'] is not None]
    # The table operates on the *raw slope* (negative = compliance);
    # since beta_1max = -slope, raw slope = -beta_1max.
    raw_1max = [-b for b in vals_1max]
    cols.append(('β(1→max)', raw_1max))

    local = compute_local_scores_for_all_languages(
        {l: d['mal_filtered'] for l, d in per_lang.items() if d['mal_filtered']})
    for n in range(1, max_n):
        key = f'{n}→{n+1}'
        vals = [s[key] for s in local.values()
                if key in s and not np.isnan(s[key])]
        cols.append((key, vals))

    cats = [
        ('β < -0.1',     '#c8e6c9', lambda v: v < -0.1),
        ('-0.1 ≤ β < 0', '#fff9c4', lambda v: -0.1 <= v < 0),
        ('0 ≤ β ≤ 0.1',  '#fff9c4', lambda v: 0 <= v <= 0.1),
        ('β > 0.1',      '#ffcdd2', lambda v: v > 0.1),
    ]

    h = ['<div class="chart-container"><h3>Slope distribution summary</h3>',
         '<table style="border-collapse:collapse;width:auto;margin:20px auto;font-size:13px;">']
    h.append('<tr style="border-bottom:2px solid #333;"><th style="padding:6px 12px;text-align:left;border:1px solid #ddd;">Category</th>')
    for label, _ in cols:
        h.append(f'<th style="padding:6px 12px;text-align:center;border:1px solid #ddd;">{label}</th>')
    h.append('</tr>')
    for label, bg, fn in cats:
        h.append(f'<tr style="background-color:{bg};">'
                 f'<td style="padding:6px 12px;border:1px solid #ddd;font-weight:bold;">{label}</td>')
        for _, vals in cols:
            h.append(f'<td style="padding:6px 12px;text-align:center;border:1px solid #ddd;">'
                     f'{sum(1 for v in vals if fn(v))}</td>')
        h.append('</tr>')
    h.append('<tr style="border-top:2px solid #333;font-weight:bold;">'
             '<td style="padding:6px 12px;border:1px solid #ddd;">Total</td>')
    for _, vals in cols:
        h.append(f'<td style="padding:6px 12px;text-align:center;border:1px solid #ddd;">{len(vals)}</td>')
    h.append('</tr></table>')
    h.append('<div class="explanation"><p>β(1→max) is the raw log-log regression slope from n=1 '
             '(negative = MAL compliance). Local change scores (1→2, 2→3, …) are positive when '
             'MAL holds at that transition.</p></div></div>')
    return '\n'.join(h)


