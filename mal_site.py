def _fisher_2x2(a, b, c, d):
    """Two-sided Fisher's exact test on a 2×2 contingency table.

    Falls back to a Python implementation if scipy is unavailable.
    Returns (odds_ratio, p_value).
    """
    try:
        from scipy.stats import fisher_exact
        return fisher_exact([[a, b], [c, d]], alternative='two-sided')
    except Exception:
        pass
    # Fallback: exact Fisher (two-sided by summing tail with prob ≤ observed).
    from math import lgamma
    def _logbinom(n, k):
        if k < 0 or k > n:
            return -float('inf')
        return lgamma(n + 1) - lgamma(k + 1) - lgamma(n - k + 1)
    def _logp(a_, b_, c_, d_):
        n_ = a_ + b_ + c_ + d_
        return (_logbinom(a_ + b_, a_) + _logbinom(c_ + d_, c_)
                - _logbinom(n_, a_ + c_))
    obs = _logp(a, b, c, d)
    row1 = a + b
    col1 = a + c
    n = a + b + c + d
    a_min = max(0, col1 - (n - row1))
    a_max = min(row1, col1)
    import math
    total = 0.0
    for a2 in range(a_min, a_max + 1):
        b2 = row1 - a2
        c2 = col1 - a2
        d2 = n - a2 - b2 - c2
        lp = _logp(a2, b2, c2, d2)
        if lp <= obs + 1e-12:
            total += math.exp(lp)
    odds = (a * d) / (b * c) if b * c else float('inf')
    return odds, total


def _significance_marker(p):
    if p is None:
        return ''
    if p < 0.001:
        return '***'
    if p < 0.01:
        return '**'
    if p < 0.05:
        return '*'
    return 'n.s.'


