def _beta_effect_label(beta):
    """Return ('MAL'|'Anti-MAL'|'gray zone', css class)."""
    if beta is None:
        return ('—', 'na-cell')
    if beta > 0.1:
        return ('MAL', 'score-positive')
    if beta < -0.1:
        return ('Anti-MAL', 'score-negative')
    return ('gray zone', 'score-neutral')


